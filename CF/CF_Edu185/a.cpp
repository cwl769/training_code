typedef long long int64;

template<typename T>
void readInt(T& x) {
    static bool f;
    x = 0;f = false;
    char c = getchar();
    for(;c<'0' || c>'9';c=getchar())if(c=='-')f=!f;
    for(;'0'<=c&&c<='9';c=getchar()) {
        x = x * 10 + c - '0';
    }
    if(f)x = -x;
}
template<typename T, typename ...Args>
void readInt(T& x, Args&... args) {
    readInt(x);
    readInt(args...);
}

int main() {
    int T;readInt(T);
    for(;T;--T) {
        int64 n;readInt(n);
        int64 ans = n*n*3ll - n - 1ll;
        if(n>2) ans = std::max(ans, std::max(n*n*4ll-n-4ll, 5ll*(n*n-n-1ll)));
        printf("%lld\n", ans);
    }

    return 0;
}