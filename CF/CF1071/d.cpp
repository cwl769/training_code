typedef long long int64;

template<typename T>
void readInt(T& x) {
    static bool f;
    x = 0;f = false;
    char c = getchar();
    for(;c<'0' || c>'9';c=getchar())if(c=='-')f=!f;
    for(;'0'<=c&&c<='9';c=getchar()) {
        x = x * 10 + c - '0';
    }
    if(f)x = -x;
}
template<typename T, typename ...Args>
void readInt(T& x, Args&... args) {
    readInt(x);
    readInt(args...);
}

int main() {
    int T;readInt(T);
    for(;T;--T) {
        int n;readInt(n);
        printf("%d ", (1<<n)-1);
        for(int len=n-1;len>=0;--len) {
            int base = (1<<len) - 1;
            int lim = (1<<(n-1-len));
            for(int i=0;i<lim;++i) {
                printf("%d ", base | (i<<(len+1)));
            }
        }
        printf("\n");
    }

    return 0;
}